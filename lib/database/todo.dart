class Todo {
  final int id;
  final String title;
  final String date;
  final bool isDone;

  Todo({required this.id, 
  required this.title, 
  required this.date, 
  required this.isDone});
}

class TodoColor{
  final int r;
  final int g;
  final int b;

  TodoColor ({required this.r, required this.g, required this.b});
}